package org.renan;

public class ContaTerminal {

    private int numero;
    private String agencia;
    private String nome;
    private double saldo;

    public ContaTerminal(int numero, String agencia, String nome, double saldo) {
        this.numero = numero;
        this.agencia = agencia;
        this.nome = nome;
        this.saldo = saldo;
    }

}
